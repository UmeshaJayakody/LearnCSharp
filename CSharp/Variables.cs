// Variables.cs
using System;

namespace MyApp
{
    class Variables
    {
        static void Main(string[] args)
        {
            // Declare and initialize variables
            int age = 30;
            string name = "John";
            bool isEmployed = true;

            // Print variable values
            Console.WriteLine($"Name: {name}");
            Console.WriteLine($"Age: {age}");
            Console.WriteLine($"Employed: {isEmployed}");
        }
    }
}